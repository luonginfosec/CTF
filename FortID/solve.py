target = (
    "<><<<>>=<>>=<>>><<>=<>>><><=<><<><=<><<<><=<>>>>=<<>><=<>><<<=<<>"
    ">=<>=<><>><=<<>><<<=<>>><>=<>>><<>=<>=<><><>>=<<>><=<>><=<>><=<<>"
    "><<=<>><<>=<<>><>=<>=<<>><><=<>><>>>=<>><<><=<>=<><<>><=<<>><=<>>"
    "><<>=<>><>>>=<>=<><>><=<<>><<<=<>>><>=<>>><<>=<>=<><<<>>=<>>><>=<"
    ">><>>>=<>><<><=<<>><><=<>><>>=<<>><=<>><>>>=<<>>=<<>><><=<<>><<=<"
    ">=<><><=<<>><=<>><<<=<>>><<>=<>><<=<>><><<=<>=<><<<<=<>><>><=<>><"
    "=<<>><<<=<>>><<>=<<>><<=<<>>=<>><><<=<>><>>=<<>><>=<>>>>>>="
)

def decode(seq):
    low, high = 0, 255
    for ch in seq:
        mid = (low + high) // 2
        if ch == '<':
            high = mid - 1
        elif ch == '>':
            low = mid + 1
        elif ch == '=':
            return chr(mid)
    return '?'


flag = ""
segment = ""
for ch in target:
    segment += ch
    if ch == '=':
        flag += decode(segment)
        segment = ""

print("Flag:", flag)
